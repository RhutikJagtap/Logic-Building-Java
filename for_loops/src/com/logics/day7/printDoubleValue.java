package com.logics.day7;

//print numbers from 1 to 10 and double each value:

public class printDoubleValue {
	
	static void printDoubleValue() {
		for(int i=1;i<=10;i++) {
			System.out.print(i*2+" ");
		}
	}
	
	public static void main(String[] args) {
		printDoubleValue();
	}

}
